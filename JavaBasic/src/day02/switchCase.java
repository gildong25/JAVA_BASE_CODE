package day02;

public class switchCase {

	public static void main(String[] args) {
		int i = 3;
		switch (i) {
		case 1:
			System.out.println("1입니다.");
			break;
		case 2:
			System.out.println("2입니다.");
			break;
		case 3:
			System.out.println("3입니다.");
			break;
		default:
			System.out.println("디폴트값입니다.");
			break;
		}
		

	}

}
